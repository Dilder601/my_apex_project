02. ON PAGE ITEM DEFAULT:

SELECT CASE WHEN TO_CHAR(SYSDATE + 6/24,'HH24:MI:SS') BETWEEN '05:00:00' AND '11:59:59' THEN 'Hi, Good Morning!,' 
            WHEN TO_CHAR(SYSDATE + 6/24,'HH24:MI:SS') BETWEEN '12:00:00' AND '15:59:59' THEN 'Hi, Good Noon!,'
            WHEN TO_CHAR(SYSDATE + 6/24,'HH24:MI:SS') BETWEEN '16:00:00' AND '16:59:59' THEN 'Hi, Good Evening!,'
            ELSE 'Hi, Good Night,'
       END GRETTINGS
FROM DUAL;