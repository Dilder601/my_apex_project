DECLARE
    vINVPREFIX  VARCHAR2(20):='INV'||TO_CHAR(SYSDATE,'YYMM');
    vINV_SL     NUMBER;
    vINV_SL_LEN NUMBER := 6;
    vINVOICE_NO VARCHAR2(20);
    vDTPREFIX   NUMBER := TO_CHAR(SYSDATE,'YYMM');
BEGIN
    vINVOICE_NO := :P9_SALE_NO;
	
	SELECT MAX(SALE_ID) --MAX(TO_NUMBER(SUBSTR(INVOICE_NO,-6)))
	INTO vINV_SL
	FROM BL_SALE_MST;
	
	vINV_SL := NVL(vINV_SL,0)+1;
	
	IF(LENGTH(vINV_SL)>vINV_SL_LEN) THEN
		vINV_SL_LEN := LENGTH(vINV_SL);
	END IF;
	
	:P9_SALE_NO := vINVPREFIX||LPAD(vINV_SL,vINV_SL_LEN,'0');
	
	--DBMS_OUTPUT.PUT_LINE('INVOICE NO: '||vINVOICE_NO);
END;


